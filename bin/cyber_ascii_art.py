        """Add consistent 4-space indentation to ASCII art."""
        lines = art.split('\n')
        indented_lines = []
        for line in lines:
            if line.strip():  # Only add indentation to non-empty lines
                indented_lines.append('    ' + line.strip())
            else:
                indented_lines.append('')
        return '\n'.join(indented_lines)
    
